function HowTo-MoveCompanyToTenant
{
<# 
.SYNOPSIS
	This Windows Powershell script shows how to create a new tenant and move a NAV company to it from another tenant/database. 
	Both the existing and the new database will be placed on the given Database Server
	Company specific as well as non-company specific data is also copied to the new tenant/database
	Users with company specific permissions in the old company as well as user that has access to all companies are copied to the new tenant/database 

.DESCRIPTION
	Company specific as well as non-company specific data is also copied to the new tenant/database
	Users with company specific permissions in the old company as well as user that has access to all companies are copied to the new tenant/database 

.PARAMETER ServerInstance
	The name of the NAV server instance
.PARAMETER FromDatabase 
	The database to copy from
.PARAMETER ToDatabase 
	The database to copy to
.PARAMETER CompanyName
	Name of the company to copy
.PARAMETER OldTenantName
	Id of the tenant in the FromDatabase
.PARAMETER NewTenantName
	Id of the new tenant which will be created
.PARAMETER ServiceAccount
	The account that the NAV server is logging in as. 
.PARAMETER DatabaseServer
	The database server name where the old tenant/database resides and where the new tenand/database will reside as well
.PARAMETER RemoveCompanyWhenMoved
	Specifies whether the old company should be deleted when it has been copied to the new tenant/database
.PARAMETER Force 
	Forces noninteractive execution
.PARAMETER Mode 
	The synchronization mode used by the new tenant

.EXAMPLE 
	HowTo-MoveCompanyToTenant -ServerInstance MyNavServer -FromDatabase 'Demo Database NAV (7-0)' -CompanyName 'CRONUS International Ltd.' -OldTenantName default -NewTenantName CRONUS1 -ServiceAccount 'NT AUTHORITY\NETWORK SERVICE' -DatabaseServer MySqlServer

.NOTES

.LINK

#>
  [CmdletBinding()]
  param (
		[parameter(Mandatory=$true)]
        [String]$ServerInstance,

		[parameter(Mandatory=$true)]
        [String]$FromDatabase,

        [parameter(Mandatory=$false)]
        [String]$ToDatabase=$NewTenantName,

        [parameter(Mandatory=$true,ValueFromPipelineByPropertyName=$true)]
        [String[]]$CompanyName,

		[parameter(Mandatory=$true)]
        [String]$OldTenantName,
        
		[parameter(Mandatory=$true)]
        [String]$NewTenantName,

		[parameter(Mandatory=$false)]
        [String]$ServiceAccount,
		
        [parameter(Mandatory=$false)]
        [String]$DatabaseServer,

		[parameter(Mandatory=$false)]
        [Switch]$RemoveCompanyWhenMoved,

        [parameter(Mandatory=$false)]
        [Switch]$Force
  )
  BEGIN
  {
    $ErrorActionPreference='Stop'
    $shouldContinue=$true

    Import-NAVManagementModule
	Import-SqlPsModule     

    if ($ServiceAccount -eq "")
    {
      $ServiceAccount = "NT AUTHORITY\NETWORK SERVICE"
    }

    if ($DatabaseServer -eq "")
    {
      $DatabaseServer = "."
    }
    
    # Check if the destination exists
    $CheckToDatabase = "SELECT name FROM master.dbo.sysdatabases WHERE ('[' + name + ']' = '$ToDatabase' OR name = '$ToDatabase')"
    Write-Verbose "Verifying destination database"
    $destination = Invoke-Sqlcmd -ServerInstance $DatabaseServer -Database master -Query $CheckToDatabase

    if($destination)
    {
        if(!($Force -or $pscmdlet.ShouldContinue("The destination database '$ToDatabase' already exists. Are you sure that you want continue?", '')))
        {
            $shouldContinue = $false
            return
        }
    }
    else
    {
        # Create the new database
        $CreateDatabase = "CREATE DATABASE "+$ToDatabase
        Write-Verbose "Creating database"
        Invoke-Sqlcmd -ServerInstance $DatabaseServer -Database master -Query $CreateDatabase

        # Create the service account as a user in the new tenant database. 
        Write-Verbose "Using service account $ServiceAccount"
        Write-Verbose "Assigning database permissions to the service account"
        $CreateServiceAccountUser = "CREATE USER [$ServiceAccount] FOR LOGIN [$ServiceAccount]"
        Invoke-Sqlcmd -ServerInstance $DatabaseServer -Database $ToDatabase -Query $CreateServiceAccountUser

        # Assign proper permissions for the service account in the new tenant database
        $AddDBOwnerPermissionToServiceAccount = "EXEC sp_addrolemember [db_owner], [$ServiceAccount]"
        Invoke-Sqlcmd -ServerInstance $DatabaseServer -Database $ToDatabase -Query $AddDBOwnerPermissionToServiceAccount 
    }

    # Mount the new tenant and create an empty company
    Write-Verbose "Mounting tenant"
    Mount-NAVTenant -Id $NewTenantName -ServerInstance $ServerInstance -DatabaseName $ToDatabase -DatabaseServer $DatabaseServer
    Write-Verbose "Initialize the new tenant"
    Sync-NAVTenant -Id $NewTenantName -ServerInstance $ServerInstance -Force
  }
  PROCESS
  {
    if(!$shouldContinue)
    {
        return
    }
    
    foreach ($company in $CompanyName)
    {
        Write-Verbose "Moving company ""$company"" from server/database (Tenant) $DatabaseServer/$FromDatabase ($OldTenantName) to new database/tenant $ToDatabase/$NewTenantName"
        Write-Verbose "Creating company ""$company"""
        New-NAVCompany -ServerInstance $ServerInstance -Tenant $NewTenantName -CompanyName $company

        #Move the data from the old to the new company
        Write-Verbose "Moving data"
        $VariableArray = "Company=$company","FromDatabase=$FromDatabase","ToDatabase=$ToDatabase"
        $sqlFile = Join-Path $PSScriptRoot 'HowTo-MoveCompanyToTenant.sql'

        Invoke-Sqlcmd -ServerInstance $DatabaseServer -Database $FromDatabase -InputFile $sqlFile -Variable $VariableArray -ConnectionTimeout 0 -QueryTimeout 65535

        if ($RemoveCompanyWhenMoved)
        {
          Write-Verbose "Dropping old company"
          Remove-NAVCompany -ServerInstance $ServerInstance -Tenant $OldTenantName -CompanyName $company -Force
        }
    }
  }
}

Export-ModuleMember -Function HowTo-MoveCompanyToTenant
